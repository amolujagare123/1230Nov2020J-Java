package InheritanceDemo;

public class Casting {

    public static void main(String[] args) {

        int i =10;
        int j = 3; // 3.3333

        System.out.println(i/j); // 3

        double c = (double) i/j; // type casting

        // casting (converting) one type of value in another
        // the value to be assigned to the another type should have
        // that perticular data type written in round bracket in font of it

        System.out.println(c);

    }

}
