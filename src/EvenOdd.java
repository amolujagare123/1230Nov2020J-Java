public class EvenOdd {

    public static void main(String[] args) {

        // print 1 to 10 numbers and also print
        // whether the number is even or odd

        for(int i=1;i<=10;i++)
        {
            // when we say the number is even or odd
            // when we say that a number is divisible by 2.
            // when the reminder is zero

            if(i % 2 == 0)
              System.out.println(i+" Even");
            else
               System.out.println(i+" Odd");

        }


    }
}
